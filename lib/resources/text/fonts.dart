import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';

class AppTextStyle {
  static TextStyle spiceShackFonts({TextStyle? style}) =>
      GoogleFonts.nokora(textStyle: style);

  static TextStyle silkScreen({TextStyle? style}) =>
      GoogleFonts.parisienne(textStyle: style);

  static TextStyle spliceSans({TextStyle? style}) =>
      GoogleFonts.splineSans(textStyle: style);

  static TextStyle quickSand({TextStyle? style}) =>
      GoogleFonts.quicksand(textStyle: style);

  static TextStyle monda({TextStyle? style}) =>
      GoogleFonts.monda(textStyle: style);

  static TextStyle akronim({TextStyle? style}) =>
      GoogleFonts.akronim(textStyle: style);
}